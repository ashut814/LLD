package com.designprinciples.lsp;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }

//     Objects of a superclass should be replaceable with objects of a subclass without affecting the correctness of the program.
//

//    Understanding the Principle
//    LSP states that subclasses should extend the behavior of a base class without breaking its functionality.
//
// Why is this Important?
//    ✅ Ensures inheritance is used properly and subclasses don’t break the behavior of the superclass.
//    ✅ Prevents unexpected bugs when using polymorphism.
//    ✅ Makes the system more predictable and maintainable.
}
